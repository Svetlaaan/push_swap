#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t j;
	size_t x;

	i = 0;
	j = 0;
	x = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[j] != '\0' && x < n)
	{
		s1[i + j] = s2[j];
		j++;
		x++;
	}
	s1[i + j] = '\0';
	return (s1);
}
