#include "libft.h"

int		ft_len_of_number_int(int n)
{
	int len;

	len = 1;
	if (n == -2147483648)
		return (10);
	if (n < 0)
		n *= -1;
	while (n > 9)
	{
		n = n / 10;
		len++;
	}
	return (len);
}